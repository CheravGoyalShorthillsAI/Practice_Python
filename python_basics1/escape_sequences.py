# Escape Sequences in Python

# Newline
new_line_example = "Hello,\nWorld!"
print("Newline Example:")
print(new_line_example)
# Output:
# Hello,
# World!

# Tab
tab_example = "Hello,\tWorld!"
print("\nTab Example:")
print(tab_example)
# Output: Hello,    World! (with a tab space)

# Backslash
backslash_example = "This is a backslash: \\"
print("\nBackslash Example:")
print(backslash_example)  # Output: This is a backslash: \

# Single Quote
single_quote_example = 'It\'s a sunny day!'
print("\nSingle Quote Example:")
print(single_quote_example)  # Output: It's a sunny day!

# Double Quote
double_quote_example = "She said, \"Hello!\""
print("\nDouble Quote Example:")
print(double_quote_example)  # Output: She said, "Hello!"

# Carriage Return
carriage_return_example = "Hello,\rWorld!"
print("\nCarriage Return Example:")
print(carriage_return_example)  # Output: World! (overwrites Hello,)

# Form Feed
form_feed_example = "Hello,\fWorld!"
print("\nForm Feed Example:")
print(form_feed_example)  # Output may depend on the console, but typically adds a page break.

# Octal Value
octal_example = "\110\145\154\154\157"  # Equivalent to "Hello"
print("\nOctal Value Example:")
print(octal_example)  # Output: Hello

